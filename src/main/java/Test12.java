public class Test12 {
    public static void main(String[] args) {

        // Explaination of post increment/decrement
        int x = 1;
        int y = x++;
        System.out.println("y= " + y);
        System.out.println("x= " + x);


        // Explaination of pre increment/decrement

        int z = 1;   //1
        int d = ++z;  // d=2 and z=2
        System.out.println("d= " + d);
        System.out.println("z= " + z);
    }
}
